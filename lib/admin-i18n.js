// Reads the admin_lang cookie. Defaults to Danish ('da') for this internal tool.
export async function getAdminLang() {
  const c = await cookies()
  return c.get('admin_lang')?.value === 'en' ? 'en' : 'da'
}

export async function getAdminT() {
  const lang = await getAdminLang()
  return { lang, t: adminTranslations[lang] }
}
